// Render every <i class="lucide-X"> by injecting an inline SVG inside it.
// We KEEP the <i> element so existing CSS rules (size, color via currentColor) still apply.
function renderIcons(root) {
  root = root || document;
  const els = root.querySelectorAll ? root.querySelectorAll('i[class*="lucide-"]') : [];
  for (const el of els) {
    if (el.dataset.iconReady === '1') continue;
    const cls = [...el.classList].find(c => c.startsWith('lucide-'));
    if (!cls) continue;
    const name = cls.slice(7);
    const inner = ICON_SVG[name];
    if (!inner) continue;
    el.innerHTML =
      '<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="none" stroke="currentColor" ' +
      'stroke-width="2" stroke-linecap="round" stroke-linejoin="round" ' +
      'style="width:1em;height:1em;vertical-align:-0.15em;display:inline-block">' + inner + '</svg>';
    el.dataset.iconReady = '1';
  }
}

window.renderIcons = renderIcons;

// Auto-render on initial DOM ready and on any subsequent insertions
function _bootIcons() {
  renderIcons(document);
  const obs = new MutationObserver(muts => {
    for (const m of muts) {
      for (const node of m.addedNodes) {
        if (node.nodeType !== 1) continue;
        const hasIcon = (node.matches && node.matches('i[class*="lucide-"]')) ||
                        (node.querySelector && node.querySelector('i[class*="lucide-"]'));
        if (hasIcon) renderIcons(node.parentNode || document);
      }
    }
  });
  obs.observe(document.body, { childList: true, subtree: true });
}

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', _bootIcons);
} else {
  _bootIcons();
}
